package com.master.project.task.beans;

public class Bluetooth {

	private int userId;
	private int blutoothId;
	private String name;
	private int type;
	private String address;

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getBlutoothId() {
		return blutoothId;
	}

	public void setBlutoothId(int blutoothId) {
		this.blutoothId = blutoothId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

}
